	<p>
		<strong>
		Why does this Web site exist?
		</strong><br />
		This work is being developed under the auspices of the Science Commons Data 
		project and builds upon the Science Commons Open Access Data Protocol proposing 
		requirements for interoperability of scientific data. Legal simplicity and 
		predictability can be achieved by waiving copyright and other contractual 
		restrictions, allowing data integrators to reuse, modify and redistribute 
		large datasets. Legal accessibility issues are not the only hurdle to data 
		integration. Technical Open Access should be ensured in order to allow 
		scientists to download data easily and use them in any way, including ways 
		that initial creators are not considered. The objective of this project is to 
		assess the accessibility of databases by analysing their interfaces to access 
		data and their reuse policies in order to identify those that are in the 
		public domain, starting with databases hosted by the <a href='http://lsrn.org/lsrn/registry.html' target='_blank'>Life Science Resource 
		Name (LSRN)</a> Schema registry.
		
	</p>
	
