<h3 id="overview">Overview of the Tool</h3>

<p>This tool is designed to gather some initial information from you and to determine,
based on that information, whether you may be eligible to terminate an
<a class="glossarylink" href="glossary.html#agreement">agreement or transfer</a>
and if so, whether your <a class="glossarylink" href="glossary.html#notice_window">"notice window"</a> is currently open.</p>

<p>Below is an overview of the questions that are asked as you complete the tool
and an outline of the kinds of information you will need to have to hand in order
to complete the tool.  Don't worry if you start working your way through the tool
and then find that you don't have all of the information available with you right now;
the tool does not record your responses from one session to the next session,
so you can stop at any time and start again.  It should take about 25 minutes to
complete the tool once you have all the relevant information.</p>

<p>If you are unsure about something the tool requires, try to answer it as best
and as accurately you can, giving as much information as you can so that this
information can be assessed by a lawyer who you consult with this information
or by a <a class="glossarylink" href="glossary.html#clinic">participating law clinic</a>.
We have tried to phrase the questions as straightforwardly as possible.
We have also provided links connected to some of the more complex terms or terms
with legal meanings, in an effort to help you better understand the nature of
the information the question is seeking and, thus, to enable you to hopefully
better answer the questions.</p>

<p>If you work through the tool and, based on the information you provide,
it seems that you are not currently eligible to terminate an agreement
in relation to a <a class="glossarylink" href="glossary.html#work">copyrighted work</a>,
do not be disheartened.  It may be that
you entered some of the information in incorrectly and/or that you need
some advice and assistance to correctly interpret and/or respond to the questions.</p>

<p>If you work through the tool and it says that you cannot currently
activate your termination right but that you may be able to do so in future,
feel free to try the tool again at that future date or to otherwise
investigate your termination options now or later.  Changes in your
circumstances over time can change what your termination options are so the
results of this tool will not hold true over time.  At any future date, you
should complete the tool again entering in the new and then current
information and/or seek new advice and assistance based on the then current information.</p>

<p>The tool is divided into the following sections &amp; these require the following
pieces of information:</p>

<div id="section_list">
<div>
<h4>Section 1 - What can be terminated?</h4>
Information required to complete this section:
<ul>
<li>Circumstances surrounding the <a class="glossarylink" href="glossary.html#work">creation of the work</a>
(eg. employment, specially commissioned etc).</li>
<li>The <a class="glossarylink" href="glossary.html#type_of_work">type of work</a></li>
<li>When the work was <a class="glossarylink" href="glossary.html#creation_date">created</a></li>
</ul>
</div>

<div>
<h4>Section 2 - When can the <a class="glossarylink" href="glossary.html#agreement">agreement or transfer</a> be terminated?</h4>
Information required to complete this section:
<ul>
<li>When the work was <a class="glossarylink" href="glossary.html#creation_date">created</a></li>
<li>When the work was <a class="glossarylink" href="glossary.html#publication_date">published</a></li>
<li>When the work was transferred or sold</a></li>
</ul>
</div>

<div>
<h4>Section 3 - Tell us about the work</h4>
Information that can be input into this section:
<ul>
<li>Identity of the <a class="glossarylink" href="glossary.html#author">author(s) or artist(s)</a></li>
<li>Title of the work (optional)</li>
<li><a class="glossarylink" href="glossary.html#registration_number">Copyright registration number</a> (optional)</li>
<li><a class="glossarylink" href="glossary.html#grantee">Original Grantee</a></li>
<li>Current  Grantee (optional)</li>
<li>Description of <a class="glossarylink" href="glossary.html#agreement">Agreement/Transfer</a></li>
</ul>
</div>

<div>
<h4>Section 4 - Who executed the agreement or transfer?</h4>
Information required to complete this section:
<ul>
<li>Was the <a class="glossarylink" href="glossary.html#agreement">agreement or transfer</a> signed by the
<a class="glossarylink" href="glossary.html#author">author(s)</a></li>
<li>Was the agreement or transfer signed by the
<a class="glossarylink" href="glossary.html#authors_family">author's spouse, widow (or widower), children, next of kin or executors & their names</a></li>
<li>Who among the author's spouse, widow (or widower), children and grandchildren are still living & their names</li>
</ul>
