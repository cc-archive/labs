  <td class="doccolumn" valign="top">
    <i>Personal files, correspondence, diaries and notes of the author or artist</i> - these documents may assist you in identifying the author(s) and artist(s), the title of the work and the original or any subsequent grantees.  The original grantee is the person or entity to whom the author(s) or artist(s) transferred the rights to the work.  Subsequent grantees are people or entities to whom the original or a prior grantee transfers the rights.<br>
    <br>
    <i>A copy of the work</i> - the copy of the work may give you information about the identity of the author(s) or artist(s) and the title of the work.  The copyright notice may also tell you who was the owner of copyright in the work (this person is probably the original or a subsequent grantee) at the time it was published. If the copy you are looking at it's a recent one, you may want to check later copies of the work or other records to see if the owner of copyright changed later on, after this copy was published.<br>
    <br>
    <i>Copyright Registration Certificate</i> - this document is issued by the US Copyright Office and contains an initial statement of copyright information about a work including the date of publication of a work, the author, the person claiming to own copyright in the work at the time of filing (this person may be the original grantee) and, importantly for this section, the copyright registration number.<br>
    <br>
    <i>US Library of Congress Catalog</i> - online searches of this catalog can provide basic information about a work including title, author, publisher (who may be an original or subsequent grantee) and date of publication (<a href="http://catalog.loc.gov/">http://catalog.loc.gov/</a>).<br>
    <br>
    <i>Royalty statements</i> - old and current royalty statements can be useful to show who is considered the author(s) or artist(s) of a work and who is a previous or current grantee (ie. person who has the benefit of exercising some rights in exchange for paying royalties).<br>
    <br>
    <i>Copyright Office records of assignments and transfers</i> - if a person executes an assignment of copyright ownership in relation to a work, they may record this with the US Copyright Office.  It is not mandatory but many people do this so that others have notice of the transfer of rights.  A research of the US Copyright Office's records of transfers may assist you in learning the identifty of an original grantee and any subsequent grantees.<br>
    <br>
    <i>Telephone directories, Internet searches, old news reports</i> - if you can identify the original grantee but are not sure if that person or entity still exists, you can search telephone directories, conduct Internet searches and review old news reports to see if a person or entity still exists.  In particular, if an original grantee was a company that was subsequently merged or acquired by another company, these records may be useful in giving you this corporate history.<br>
    <br>
    <i>The agreement or transfer</i> - the agreement will often have either its title or somewhere in its introductory paragraphs a description of the nature of the transfer (eg. assignment, exclusive license, nonexclusive license, mortgage, security etc.).<br>
